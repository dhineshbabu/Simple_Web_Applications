let number = -10;
console.log(Math.sign(number)); //-1
let number1 = 10;
console.log(Math.sign(number1)); //1
let number2 = 0;
console.log(Math.sign(number2)); //0
let number3 = NaN;
console.log(Math.sign(number3)); //NaN

let decimal = 3.78;
console.log(Math.trunc(decimal)); //3
console.log(Math.floor(decimal)); //3
console.log(Math.ceil(decimal)); //4
